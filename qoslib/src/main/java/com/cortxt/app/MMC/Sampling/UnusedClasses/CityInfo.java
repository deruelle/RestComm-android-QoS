package com.cortxt.app.MMC.Sampling.UnusedClasses;

public class CityInfo {
	private String name;
	private int id;
	
	public CityInfo(String name, int id) {
		this.name = name;
		this.id = id;
	}
	
	public void setName(String name) {
		this.name = name;
	}
	
	public String getName() {
		return name;
	}
	
	public void setId(int id) {
		this.id = id;
	}
	
	public int getId() {
		return id;
	}
}